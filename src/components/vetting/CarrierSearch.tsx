import { useState, useCallback, useEffect, useRef } from 'react';
import { Search, Building2, Loader2, Hash, Truck } from 'lucide-react';
import { Input } from '@/components/ui/input';
import { Button } from '@/components/ui/button';
import { cn } from '@/lib/utils';


interface SearchResult {
  dotNumber: string;
  legalName: string;
  dbaName: string;
  city: string;
  state: string;
  phone: string;
}

interface CarrierSearchProps {
  onSelect: (dotNumber: string) => void;
  className?: string;
  isLoading?: boolean;
}

export function CarrierSearch({ onSelect, className, isLoading: externalLoading }: CarrierSearchProps) {
  const [query, setQuery] = useState('');
  const [searchType, setSearchType] = useState<'name' | 'dot' | 'mc'>('name');
  const [results, setResults] = useState<SearchResult[]>([]);
  const [isSearching, setIsSearching] = useState(false);
  const [showResults, setShowResults] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const containerRef = useRef<HTMLDivElement>(null);
  const debounceRef = useRef<NodeJS.Timeout>();
  
  const isLoading = isSearching || externalLoading;

  const search = useCallback(async (searchQuery: string, type: 'name' | 'dot' | 'mc') => {
    if (!searchQuery.trim() || searchQuery.length < 2) {
      setResults([]);
      return;
    }

    setIsSearching(true);
    setError(null);

    try {
      const response = await fetch(
        `${import.meta.env.VITE_SUPABASE_URL}/functions/v1/carrier-lookup?type=${type}&q=${encodeURIComponent(searchQuery)}`,
        {
          headers: {
            'Authorization': `Bearer ${import.meta.env.VITE_SUPABASE_PUBLISHABLE_KEY}`,
            'Content-Type': 'application/json',
          },
        }
      );

      const data = await response.json();

      if (!response.ok) {
        setError(data.error || 'Search failed');
        setResults([]);
        setShowResults(true);
        return;
      }

      if (type === 'name') {
        const resultList = data.results || [];
        setResults(resultList);
        if (resultList.length === 0) {
          setError('No carriers found matching that name');
        }
      } else {
        // DOT or MC search returns results array
        const resultList = data.results || [];
        if (resultList.length > 0) {
          setResults(resultList);
        } else if (data.dotNumber) {
          setResults([data]);
        } else {
          setResults([]);
          setError(type === 'mc' ? 'No carrier found with that MC number' : 'No carrier found with that DOT number');
        }
      }
      setShowResults(true);
    } catch (err) {
      console.error('Search error:', err);
      setError('Failed to connect to FMCSA database. Please try again.');
      setResults([]);
    } finally {
      setIsSearching(false);
    }
  }, []);

  useEffect(() => {
    if (debounceRef.current) {
      clearTimeout(debounceRef.current);
    }

    if (query.length >= 2) {
      debounceRef.current = setTimeout(() => {
        search(query, searchType);
      }, 300);
    } else {
      setResults([]);
      setShowResults(false);
    }

    return () => {
      if (debounceRef.current) {
        clearTimeout(debounceRef.current);
      }
    };
  }, [query, searchType, search]);

  // Close dropdown when clicking outside
  useEffect(() => {
    function handleClickOutside(event: MouseEvent) {
      if (containerRef.current && !containerRef.current.contains(event.target as Node)) {
        setShowResults(false);
      }
    }

    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, []);

  const handleSelect = (result: SearchResult) => {
    onSelect(result.dotNumber);
    setQuery('');
    setResults([]);
    setShowResults(false);
  };

  // Detect if in sidebar context
  const isSidebar = className?.includes('sidebar-search');

  return (
    <div ref={containerRef} className={cn('relative', className)}>
      <div className="space-y-3">
        {/* Search Type Toggle - Compact buttons for sidebar, full for terminal */}
        <div className={cn('flex gap-1.5', isSidebar ? 'flex-wrap' : 'gap-2')}>
          <Button
            type="button"
            variant="ghost"
            size="sm"
            className={cn(
              'rounded-md font-medium transition-all active:scale-95',
              isSidebar ? 'h-7 px-2 text-xs flex-1 min-w-0' : 'h-9 px-3 text-sm',
              isSidebar
                ? (searchType === 'name' 
                    ? 'bg-primary/10 text-primary border-2 border-primary font-semibold shadow-sm' 
                    : 'bg-transparent text-muted-foreground border border-border hover:bg-primary/5 hover:text-primary hover:border-primary/50 active:bg-primary/10')
                : (searchType === 'name' 
                    ? 'text-white border-2 border-green-500 shadow-[0_0_8px_rgba(34,197,94,0.3)]' 
                    : 'text-white/60 hover:text-white hover:bg-white/10 border border-white/20 active:bg-white/20')
            )}
            onClick={() => setSearchType('name')}
          >
            <Building2 className={cn(isSidebar ? 'w-3 h-3 mr-1' : 'w-4 h-4 mr-1.5')} />
            Name
          </Button>
          <Button
            type="button"
            variant="ghost"
            size="sm"
            className={cn(
              'rounded-md font-medium transition-all active:scale-95',
              isSidebar ? 'h-7 px-2 text-xs flex-1 min-w-0' : 'h-9 px-3 text-sm',
              isSidebar
                ? (searchType === 'dot' 
                    ? 'bg-primary/10 text-primary border-2 border-primary font-semibold shadow-sm' 
                    : 'bg-transparent text-muted-foreground border border-border hover:bg-primary/5 hover:text-primary hover:border-primary/50 active:bg-primary/10')
                : (searchType === 'dot' 
                    ? 'text-white border-2 border-green-500 shadow-[0_0_8px_rgba(34,197,94,0.3)]' 
                    : 'text-white/60 hover:text-white hover:bg-white/10 border border-white/20 active:bg-white/20')
            )}
            onClick={() => setSearchType('dot')}
          >
            <Hash className={cn(isSidebar ? 'w-3 h-3 mr-1' : 'w-4 h-4 mr-1.5')} />
            DOT
          </Button>
          <Button
            type="button"
            variant="ghost"
            size="sm"
            className={cn(
              'rounded-md font-medium transition-all active:scale-95',
              isSidebar ? 'h-7 px-2 text-xs flex-1 min-w-0' : 'h-9 px-3 text-sm',
              isSidebar
                ? (searchType === 'mc' 
                    ? 'bg-primary/10 text-primary border-2 border-primary font-semibold shadow-sm' 
                    : 'bg-transparent text-muted-foreground border border-border hover:bg-primary/5 hover:text-primary hover:border-primary/50 active:bg-primary/10')
                : (searchType === 'mc' 
                    ? 'text-white border-2 border-green-500 shadow-[0_0_8px_rgba(34,197,94,0.3)]' 
                    : 'text-white/60 hover:text-white hover:bg-white/10 border border-white/20 active:bg-white/20')
            )}
            onClick={() => setSearchType('mc')}
          >
            <Truck className={cn(isSidebar ? 'w-3 h-3 mr-1' : 'w-4 h-4 mr-1.5')} />
            MC
          </Button>
        </div>

        {/* Search Input - Full Width */}
        <div className="relative">
          {isSidebar ? (
            <>
              <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
              <Input
                type="text"
                placeholder={
                  searchType === 'name' 
                    ? 'Company name...' 
                    : searchType === 'dot' 
                      ? 'DOT number...' 
                      : 'MC number...'
                }
                value={query}
                onChange={(e) => setQuery(e.target.value)}
                onFocus={() => results.length > 0 && setShowResults(true)}
                className="pl-9 pr-8 h-9 text-sm"
              />
              {isLoading && (
                <Loader2 className="absolute right-3 top-1/2 -translate-y-1/2 w-4 h-4 text-primary animate-spin" />
              )}
            </>
          ) : (
            <>
              <Search className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-white/50" />
              <Input
                type="text"
                placeholder={
                  searchType === 'name' 
                    ? 'Search carrier by company name...' 
                    : searchType === 'dot' 
                      ? 'Enter DOT number...' 
                      : 'Enter MC number...'
                }
                value={query}
                onChange={(e) => setQuery(e.target.value)}
                onFocus={() => results.length > 0 && setShowResults(true)}
                className="pl-12 pr-12 h-12 text-base bg-white/5 border-white/20 text-white placeholder:text-white/40 focus:border-primary/50 focus:ring-primary/20"
              />
              {isLoading && (
                <div className="absolute right-4 top-1/2 -translate-y-1/2 flex items-center gap-2">
                  <Loader2 className="w-5 h-5 text-primary animate-spin" />
                  <span className="text-xs text-primary font-mono hidden sm:inline">Querying FMCSA.gov...</span>
                </div>
              )}
            </>
          )}
        </div>
      </div>

      {/* Results Dropdown */}
      {showResults && (results.length > 0 || error) && (
        <div className="absolute z-50 top-full left-0 right-0 mt-2 bg-card border border-border rounded-lg shadow-xl overflow-hidden">
          {error ? (
            <div className="p-4 text-center text-sm text-muted-foreground">
              {error}
            </div>
          ) : (
            <div className="max-h-80 overflow-y-auto">
              {results.map((result) => (
                <button
                  key={result.dotNumber}
                  type="button"
                  className="w-full px-4 py-3 text-left hover:bg-muted/50 transition-colors border-b border-border/50 last:border-0"
                  onClick={() => handleSelect(result)}
                >
                  <div className="flex items-start justify-between gap-2">
                    <div className="flex-1 min-w-0">
                      <p className="font-medium text-sm truncate">{result.legalName}</p>
                      {result.dbaName && result.dbaName !== result.legalName && (
                        <p className="text-xs text-muted-foreground truncate">DBA: {result.dbaName}</p>
                      )}
                      <p className="text-xs text-muted-foreground mt-0.5">
                        {result.city}, {result.state}
                      </p>
                    </div>
                    <span className="text-xs text-muted-foreground bg-muted px-2 py-0.5 rounded">
                      DOT# {result.dotNumber}
                    </span>
                  </div>
                </button>
              ))}
            </div>
          )}
        </div>
      )}
    </div>
  );
}
